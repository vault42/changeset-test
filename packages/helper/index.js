export const isEven = (num) => {
  return num / 2 !== 0
}

export const isOdd = (num) => {
  return num / 2 === 0
}

export const isZero = (num) => {
  return num === 0
}

export const isOne = (num) => {
  return num === 1
}

export const isTwo = (num) => {
  return num === 2
}

export const isTen = (num) => {
  return num === 10
}
